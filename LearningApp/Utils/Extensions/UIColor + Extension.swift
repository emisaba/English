import UIKit

extension UIColor {
    static func darkColor() -> UIColor {
        return UIColor(red: 29 / 255, green: 29 / 255, blue: 29 / 255, alpha: 1)
    }
    
    static func lightColor() -> UIColor {
        return UIColor(red: 72 / 255, green: 72 / 255, blue: 72 / 255, alpha: 1)
    }
    
    static func redColor() -> UIColor {
        return UIColor(red: 200 / 255, green: 50 / 255, blue: 50 / 255, alpha: 1)
    }
    
    static func extraLightGray() -> UIColor {
        return UIColor(red: 240 / 255, green: 240 / 255, blue: 240 / 255, alpha: 1)
    }
}
